from marshmallow import Schema, fields, validate

class StudentSchema(Schema):
    """Schema for serializing and validating Student data."""

    id = fields.Int(dump_only=True)  # Field is generated by the database, only included in output
    name = fields.Str(required=True, validate=validate.Length(min=1))  # Name must be a non-empty string

    class Meta:
        # Meta class to customize the behavior of the schema
        ordered = True  # Maintain the order of the fields as defined in the schema

# Instantiate the schema
student_schema = StudentSchema()  # To be used for serialization and deserialization
student_list_schema = StudentSchema(many=True)  # To handle list of students for serialization and deserialization